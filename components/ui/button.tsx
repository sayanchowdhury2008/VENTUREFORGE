import * as React from "react"
import { Slot } from "@radix-ui/react-slot"
import { cva, type VariantProps } from "class-variance-authority"
import { cn } from "@/lib/utils"

const buttonVariants = cva(
  "inline-flex items-center justify-center whitespace-nowrap text-sm font-medium ring-offset-background transition-colors focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-ring focus-visible:ring-offset-2 disabled:pointer-events-none disabled:opacity-50",
  {
    variants: {
      variant: {
        default: "brutalist-button",
        destructive: "bg-red-500 text-white border-4 border-black harsh-shadow-sm hover:harsh-shadow font-black uppercase",
        outline: "border-4 border-black bg-white text-black hover:bg-yellow-400 font-black uppercase harsh-shadow-sm",
        secondary: "bg-cyan-400 text-black border-4 border-black harsh-shadow-sm hover:harsh-shadow font-black uppercase",
        ghost: "hover:bg-yellow-400 hover:text-black font-bold uppercase border-2 border-transparent hover:border-black",
        link: "text-black underline-offset-4 hover:underline font-bold uppercase",
      },
      size: {
        default: "h-10 px-4 py-2",
        sm: "h-9 px-3",
        lg: "h-11 px-8",
        icon: "h-10 w-10",
      },
    },
    defaultVariants: {
      variant: "default",
      size: "default",
    },
  }
)

export interface ButtonProps
  extends React.ButtonHTMLAttributes<HTMLButtonElement>,
    VariantProps<typeof buttonVariants> {
  asChild?: boolean
}

const Button = React.forwardRef<HTMLButtonElement, ButtonProps>(
  ({ className, variant, size, asChild = false, ...props }, ref) => {
    const Comp = asChild ? Slot : "button"
    return (
      <Comp
        className={cn(buttonVariants({ variant, size, className }))}
        ref={ref}
        {...props}
      />
    )
  }
)
Button.displayName = "Button"

export { Button, buttonVariants }
